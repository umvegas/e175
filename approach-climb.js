function slider(label, min, max, step, value, inputFun, makeReflector) {
    var updateLabel;
    return ['div',
            ['style', ['margin', '2em 0']],
            ['div',
             ['style',
              ['textAlign', 'center']],
             ['with', valueDisplay => {
                 updateLabel = t => {
                     valueDisplay.innerHTML = label + ': ' + t;
                 };
             }]],
            ['input',
             ['attr',
              ['class', 'slider'],
              ['type', 'range'],
              ['min', min],
              ['max', max],
              ['step', step],
              ['value', value]],
             ['style',
              ['width', '100%']],
             ['with', s => {
                 updateLabel(value);
                 inputFun(value);
                 if (makeReflector) {
                     makeReflector(reflectedValue => {
                         s.value = reflectedValue;
                         updateLabel(reflectedValue);
                         inputFun(reflectedValue);
                     });
                 }
             }],
             ['on',
              ['input', e => {
                  updateLabel(e.target.value);
                  inputFun(e.target.value);
              }]]]];
}
function flapButtons() {
    var dim, pick = [];
    function flapButton(setting) {
        return ['div', 'Flaps ' + setting,
                ['style',
                 ['display', 'inline-block'],
                 ['border', '2px solid gray'],
                 ['padding', '.6em 1em'],
                 ['marginLeft', '1em'],
                 ['borderRadius', '8px']],
                ['with', btn => {
                    function pickMe() {
                        dim && dim();
                        btn.style.background = 'lightgreen';
                        dim = () => btn.style.background = 'none';
                        lookup({ flaps : setting });
                    }
                    pick[setting] = pickMe;
                    M(['on', ['click', e => {
                        pickMe();
                    }]], btn);
                }]];
    }
    M([flapButton, 2], document.body);
    M([flapButton, 4], document.body);
    pick[2]();
}
